function G = get_G(z,d)
	ab = d;
	ca = z;
	g1 = @(x) (0.46 + 3.06*x + 0.84*(1-x).^5 + 0.66*x.^2.*(1-x).^2);
	g2 = @(x) (-3.52*x.^2);
	g3 = @(x) (6.17 - 28.22*x + 34.54*x.^2 - 14.39*x.^3 - (1-x).^(1.5) - 5.88*(1-x).^5 - 2.64*x.^2 .* (1-x).^2);
	g4 = @(x) (-6.63 + 25.16*x - 31.04*x.^2 + 14.41*x.^3 + 2*(1-x).^(1.5) + 5.04*(1-x).^5 + 1.98*x.^2 .* (1-x).^2);
	G =  g1(ab) + g2(ab) .* ca + g3(ab) .* ca.^2 + g4(ab) .* ca.^3;
end
